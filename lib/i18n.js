function preserveSpacing(original, translated) {
  const leading = original.match(/^\s*/)?.[0] || "";
  const trailing = original.match(/\s*$/)?.[0] || "";
  return `${leading}${translated}${trailing}`;
}

export function translateString(value, locale = "fr") {
  const original = `${value ?? ""}`;
  if (!original || locale !== "en") return original;
  const trimmed = original.trim();
  if (!trimmed) return original;

  const exact = EXACT_EN[trimmed];
  if (exact) {
    return preserveSpacing(original, exact);
  }

  for (const [pattern, replacement] of REGEX_RULES_EN) {
    if (pattern.test(trimmed)) {
      return preserveSpacing(original, trimmed.replace(pattern, replacement));
    }
  }

  return original;
}

function translateTextNode(node, locale) {
  if (!node) return;
  if (!node.__flowOriginalText) {
    node.__flowOriginalText = node.nodeValue || "";
  }
  const nextValue = translateString(node.__flowOriginalText, locale);
  if (node.nodeValue !== nextValue) {
    node.nodeValue = nextValue;
  }
}

function translateElementAttributes(element, locale) {
  if (
    !element
    || element.isContentEditable
    || element.closest?.("[contenteditable='true']")
  ) {
    return;
  }
  ATTRIBUTE_KEYS.forEach((key) => {
    const current = element.getAttribute(key);
    if (current == null) return;
    const dataKey = `flowI18nOriginal${key.replace(/(^|-)([a-z])/g, (_, __, c) => c.toUpperCase())}`;
    if (!element.dataset[dataKey]) {
      element.dataset[dataKey] = current;
    }
    const nextValue = translateString(element.dataset[dataKey], locale);
    if (element.getAttribute(key) !== nextValue) {
      element.setAttribute(key, nextValue);
    }
  });
}

export function translateDomTree(root, locale = "fr") {
  if (!root) return;
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
  let textNode = walker.nextNode();
  while (textNode) {
    if (
      textNode.parentElement
      && !["SCRIPT", "STYLE", "INPUT", "TEXTAREA", "SELECT", "OPTION"].includes(textNode.parentElement.tagName)
      && !textNode.parentElement.isContentEditable
      && !textNode.parentElement.closest?.("[contenteditable='true']")
    ) {
      translateTextNode(textNode, locale);
    }
    textNode = walker.nextNode();
  }

  const elements = root.querySelectorAll?.("*") || [];
  elements.forEach((element) => translateElementAttributes(element, locale));
}

export function installDomTranslator(root, locale = "fr") {
  if (typeof window === "undefined" || !root) return () => {};
  translateDomTree(root, locale);
  let scheduled = false;
  const observer = new MutationObserver(() => {
    if (scheduled) return;
    scheduled = true;
    window.requestAnimationFrame(() => {
      scheduled = false;
      translateDomTree(root, locale);
    });
  });
  observer.observe(root, {
    childList: true,
    subtree: true,
  });
  return () => observer.disconnect();
}

export function getStoredLocale() {
  if (typeof window === "undefined") return "fr";
  const raw = window.localStorage.getItem("flow_locale");
  return raw === "en" ? "en" : "fr";
}

export function storeLocale(locale) {
  if (typeof window === "undefined") return;
  window.localStorage.setItem("flow_locale", locale === "en" ? "en" : "fr");
}
